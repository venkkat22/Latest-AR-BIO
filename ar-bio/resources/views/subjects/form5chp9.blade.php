	</div>
	<div class="mybt">
		<h4>Collonisation and succession:</h4>

		<li>Collonisation: plants start to conquer an uninhabited area,breed and form colonies in that area
		</li>
			<li>Succession: a few species of dominant plants in a habitat are gardually being replaced by other species called successor
		</li>
	</div>
	<div class="mybt">
		<h4>The importance of the mangrove ecosystem:</h4>
<br>
<h4>Protection zone:</h4>

		<li>Mangrove swamps serve as a natural buffer to reduce the speed of waves and winds that reach the coastal areas.
		</li>	<li>The mangrove swamp forest is a refuge for small fish, shrimp and crabs from predators and the movement of water currents and strong waves.
		</li>
		<li>Mangrove swamps serve as sanctuaries and food sources for various species of migrating birds.
		</li>

		<br>
		<h4>Fishery resources:</h4>
	
		<li>Seafood such as fish, shrimp, crabs and snails are a source of income for fishermen in mangrove swamps.
		</li>	<li>The waters in the mangrove swamps support the floating cage industry to breed commercial species.
		</li>
		<br>

					<h4>Forestry resources:</h4>

		<li>Mangrove wood can be used to make canoes, fish traps and building frames.
		</li>	<li>Mangrove wood is burned in a furnace to produce a source of fuel, namely charcoal.
		</li>
