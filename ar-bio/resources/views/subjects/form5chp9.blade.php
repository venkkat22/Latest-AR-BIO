	<div class="inbt" style="background-color: yellow;">
		<h3 style>9.1 Community and Ecosystem
		</h3>
</div>
<div class="mybt">
	<li>Organisms not only interact with each other, but also interact with non -living things to form a balanced system known as an ecosystem.</li>
</div>
<div class="mybt">

	<h4>Habitat:</h4>
<li>Habitat is the natural environment or abode of an organism</li>
<li>Bird habitat in the forest
</li>
<li>Habitat of frogs in ponds
</li>
</div>
<div class="mybt">
	<h4>Species:</h4>
<li>A species is a group of similar organisms, able to reproduce and produce offspring
</li>
</div>
<div class="mybt">
	<h4>Population:</h4>

<li>A population is a group of organisms of the same species and living in the same habitat
</li>
</div>
<div class="mybt">
	<h4>Community:</h4>

<li>Communities are all populations of organisms of different species that live in one habitat and interact with each other.
</li>

</div>
<div class="mybt">
	<h4>Ecosystem:</h4>
<li>An ecosystem is a number of communities that live together in one habitat, interacting with each other including with non -living components (abiosis) such as water, air and soil.
</li>
</div>
<div class="mybt">
	<h4>Niche:</h4>
	1. Niche is the role of an organism in an ecosystem that includes its behavior and interaction with the components of biosis and abiosis in its habitat environment.

	<li>Ecological niche: the role of a species in its surroundings
	</li>
<li>Species niche: the way in which a species interacts with biotic and abiotic components within its surroundings
</li>
</div>
<div class="mybt">
	<h4>pH value:</h4>
<li>The pH value of the soil greatly affects the distribution of organisms in a habitat
</li>
<li>Most suitable organisms live in neutral or near -neutral pH conditions.
</li><li>Soil is home to hundreds of millions of worms and microorganisms such as bacteria, fungi and protozoa.
</li>
</div>
<div class="mybt">
	<h4>Temperature:</h4>
<li>Ambient temperature affects the physiological activities of plants and animals.
</li><li>Small temperature changes will lower the rate of metabolism because all the enzymes that catalyze physiological responses are very sensitive to temperature changes.
</li><li>Although most organisms can survive in a temperature range between 20 degrees Celsius to 40 degrees Celsius, there are also organisms that can survive in extreme temperatures.
</li><li>Polar bears can live in tundra habitats with temperatures of -14 degrees Celsius while foxes can live in the desert with temperatures reaching 45 degrees Celsius during the day.
</li></div>
<div class="mybt">
	<h4>Light intensity:</h4>
	<li>The intensity of light and the length of time that sunlight radiates in an area greatly affects the distribution of organisms, especially plants that carry out photosynthesis.
	</li><li>Tall trees exposed to high light intensity in tropical rainforest areas form a canopy of low light intensity below.
	</li><li>Only small plants such as ferns can live under this canopy
	</li>
	<li>Moderate climate coniferous forests with low light intensity have less plant density.
	</li>
</div>
<div class="mybt">
	<h4>Topography:</h4>
	<li>Topography is the physical feature of the earth's surface that includes altitude, gradient and aspect
	</li>	<li>Topography determines humidity, temperature, and light intensity in an ecosystem.
	</li>
</div>
<div class="mybt">
	<h4>Microclimate:</h4>
		<li>Microclimate refers to the climatic conditions of a small area that are different from the surrounding area
		</li>
		
		<li>The microclimate may occur under rocks or under large thickets of trees in the forest canopy
		</li>
		<li>The microclimate depends on temperature, humidity, light intensity, heat balance, atmospheric pressure, water evaporation as well as the ability of the soil to retain water in the area to retain moisture.
		</li>
	</div>
	<div class="mybt">
		<h4>Autotrophic and heterotrophic nutrition:</h4>
		<li>Nutrition is the way organisms obtain nutrients and energy from food eaten for life processes
		</li>

		<li>There are two types of nutrients, namely autotrophic and heterotrophic.
		</li>

	</div>
	<div class="mybt">
		<h4>Autotrophic nutrition:</h4>
		<li>Photoautotrophic
		</li>
		<li>Chemoautotrophic</li>

	</div>
	<div class="mybt">
		<h4>Photoautotrophic:</h4>
		<li>Photoautotropic are organisms that synthesize organic compounds from carbon dioxide and light energy.
		</li>

		<li>Photoautotrophic can synthesize food on their own through the process of photosynthesis
		</li>
		<li>Example: Green plants
		</li>
		
	</div>
	<div class="mybt">
		<h4>Chemoautotrophic:</h4>
		<li>Chemoautotrophic include several types of bacteria that synthesize organic compounds without the use of light.
		</li>
		<li>Chemoautotrophic obtain energy through the oxidation of inorganic substances such as hydrogen sulfide and ammonia through chemosynthesis
		</li>
		<li>Example: Nitrobacter sp.
		</li>
		
	</div>
	<div class="mybt">
		<h4>Heterotrophic:</h4>
		<li>Saprotrophic
		</li>
		<li>Holozoic</li>
		<li>Parasitic</li>

		
	</div>
	<div class="mybt">
		<h4>Saprotrophic:</h4>
		<li>Saprotrophic are saprophytic organisms that obtain nutrients from dead and decaying organic matter.\
		</li>
		<li>Digestion is done outside the body of the organism before nutrients are absorbed into the body.
		</li>
		<li>Example: Fungi
		</li>
	</div>
	<div class="mybt">
		<h4>Holozoic:</h4>
		<li>Organisms that consume solid organic matter which then digests it and is absorbed into the body.
		</li>
		<li>Humans and almost all types of animals are holozoic.
		</li>
		<li>Example: Squirrel
		</li>
	</div>
	<div class="mybt">
		<h4>Parasitic:</h4>

		<li>Parasitic are organisms that absorb nutrients from a host.
		</li>
		<li>For example, fleas and tapeworms that absorb nutrients from their hosts are humans.
		</li>
		<li>Example: Fleas
		</li>

	</div>
	<div class="mybt">
		<h4>Biotic components according to trophic levels:</h4>

		<li>The biotic component consists of organisms that need energy to carry out life processes.
		</li>
		<li>Humans and animals obtain energy by eating other organisms including photosynthetic plants.
		</li>
		<li>Therefore, the most important source of energy for all organisms comes from the sun.
		</li>
		<li>The components of biosis consist of three groups, namely producers, consumers and decomposers
		</li>


		
	</div>
	<div class="mybt">
		<h4>Energy flow in the food chain:</h4>

		<li>Organisms in an ecosystem interact with each other in the form of nutritional relationships.
		</li>
		<li>Such relationships are shown in the food chain.
		</li>
		<li>The food chain is a sequence of energy transfers from one trophic level to the next trophic level, starting from the producer.
		</li>

	</div>
	<div class="mybt">
		<h4>In a food chain:</h4>

		<li>It starts with the producer and ends with the secondary consumers or the tertiary consumers
		</li>
		<li>The organism will eat the organism from the previous trophic level
		</li>
		<li>Energy is transferred from the organism that eats it to the organism that eats it
		</li>
		<li>Organisms that eat other organisms get energy from the body tissues of the organism money is eaten. This energy is transferred to the organism when food is digested and assimilated to form new tissues
		</li>

	</div>
	<div class="mybt">
		<h4>In a food web:</h4>

		<li>Describe the nutritional relationship in a community
		</li>
		<li>Organisms in all food chains are interdependent with each other in aspects of nutrition
		</li>
		<li>Initiated by producers who photosynthesize and convert light energy from the sun into chemical energy in the form of food stored in root organs, fruits, stems or leaves.
		</li>
		
	</div>
	<div class="mybt">
		<h4>Ecological pyramids:</h4>

		<li>Food chains and food webs describe nutritional relationships between organisms.
		</li>
		<li>When one organism eats another organism, energy transfer occurs.
		</li>
		<li>In a nutritional interaction, as the trophy level increases, the number of individuals, biomass and the amount of energy contained in each individual for each trophy level will change.
		</li>
			<li>All these factors can be described in the form of an ecological pyramid consisting of a number pyramid, a biomass pyramid and an energy pyramid.
			</li>

		</div>
		<div class="mybt">
			<h4>Pyramid of numbers:</h4>
	
		<li>The bottom most has the largest number and is the first trophic level that represents the number of producers
		</li>
		<li>The next part of the pyramid is the second, third and fourth trophic levels which represent the number of primary users, secondary users and tertiary users
		</li>
		<li>The next part of the pyramid is the second, third and fourth trophic levels which represent the number of primary users, secondary users and tertiary users
		</li>
		
	</div>
	<div class="mybt">
		<h4>Pyramid of biomass:</h4>

		<li>The pyramid also depicts the biomass that can be supplied to organisms in the next trophic level
		</li>
		<li>For example, the amount of producer biomass that can be consumed by primary consumers is higher than the primary consumer biomass in the ecosystem
		</li>	
		<li>Similarly the total biomass of secondary consumers is lower than that of primary consumers
		</li>
		<li>The higher to the top of the pyramid, the less the amount of biomass per unit area
		</li>

			
	</div>
	<div class="mybt">
		<h4>Pyramid of energy:</h4>

		<li>The source of energy in an ecosystem is light energy from the sun that is absorbed by green plants to carry out photosynthesis and convert it into chemical energy.
		</li>
		<li>Energy will be transferred to the next trophy level when the primary consumer consumes the producer plant
		</li>
		<li>The energy present in food molecules may be stored in body tissues or transferred to the environment in the form of excrement such as urine or excreted through feces.
		</li>
		<li>When food molecules are broken down for respiration and other reactions, some energy is released into the environment through heat. Only a small portion of the energy in food is converted to energy stored in body tissues as a supplement to the organism's biomass.
		</li>
				<li>Therefore, organisms at lower trophic levels have more available energy content than organisms at higher trophic levels.
				</li>

			</div>
			<div class="mybt">
				<h4>Saprophytism:</h4>

			<li>Saprophytism
				</li>
			<li>Symbiosis
				</li>

			</div>
			<div class="mybt">
				<h4>Types of interaction among biotic componentsy:</h4>

		<li>Interactions of organisms that obtain food from dead organic matter
		</li>
		<li>For example, mushrooms that grow on dead trees
		</li>


	</div>
	<div class="mybt">
		<h4>Symbiosis:</h4>

		<li>Symbiosis occurs when different species live together and interact with each other.
		</li>		
		<li>Mutualism</li>
		<li>Commensalism</li>
		<li>Parasitism</li>

		
	</div>
	<div class="mybt">
		<h4>Predation:</h4>

		<li>Involves one organism (predator) eating another organism (prey)
		</li>	
				
	</div>
	<div class="mybt">
		<h4>Competition:</h4>
	

		<li>Occurs when organisms in one habitat compete for basic needs
		</li>
	</div>
	<div class="mybt">
		<h4>Mnagrove ecosystem:</h4>
	

		<li>Abiotic components</li>
		<li>Biotic components</li>

