	<div class="mybt">
		<h4>Abiotuc components of mangrove ecosystems:</h4>
	

		<li>Exposed to waves and tides</li>	
		<li>Soft, slippery, muddy and poorly ventilated soil</li>
		<li>Exposed to strong winds</li>
		<li>High salt content in the soil and very low concentration of dissolved oxygen in the water</li>	
		<li>Exposed to high light intensity</li>
		<li>Seed germination in tidal areas</li>
	</div>
	<div class="mybt">
		<h4>Biotic components:</h4>
		<li>Stork</li>
