	<div class="mybt">
		<h3>Insulin :</h3>
		<li>The hormone insulin plays an important role in controlling blood sugar levels.</li>
		<li>In the past, insulin was extracted from the pancreas of cattle or pigs to treat patients with diabetes mellitus.</li>
		<li>Now, insulin can be obtained from genetic engineering techniques and can be produced commercially for the use of diabetics</li>
		<li>In addition to insulin, hepatitis B vaccine, blood clotting factors and growth hormone have been successfully genetically engineered.</li>
