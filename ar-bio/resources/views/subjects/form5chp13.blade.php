<div class="mybt">
	<h3>Genetically Modified Organism (GMO):</h3>
		<li>Genetic engineering is a gene manipulation technique to genetically modify an organism to form new gene combinations.</li>
		<li>Organisms whether animals, plants or microorganisms produced through recombinant DNA technology are known as genetically modified organisms or genetically modified organisms (GMOs).</li>
		<li>Genetic engineering involves the transfer of DNA segments from one organism to another through recombinant DNA technology.</li>
		<li>By using techniques and procedures in recombinant DNA technology, biologists can reconstruct the DNA or genome (a complete set of DNA) of an organism.</li>
		<li>Genetically modified organisms (GMOs) are organisms that contain recombinant DNA.</li>
		<li>Through recombinant DNA technology, new combinations of genes are produced in an organism</li>
		<li>Organisms that contain recombinant DNA are known as transgenic organisms</li>
</div>
<div class="mybt">
	<h3>Genetically Modified Food (GMF) :</h3><br>
	<li>Recombinant DNA technology has successfully produced many crops (rice, oil palm, pineapple, corn and soybeans) as well as livestock (salmon, cattle and goats) that have the desired characteristics.</li>
	<li>Genetically modified foods (GMF) have DNA from other plant or animal species.</li>
	<li>Intake of GMF by humans is likely to have a variety of still unknown health implications.</li>
</div>

<div class="mybt">
	<h3>Characteristics of genetically modified food (GMF) :</h3><br>
	<li>Resistant to herbicide</li>
	<li>Resistant to disease</li>
	<li>Application in medicine</li>
	<li>Tolerance to heavy metals</li>
	<li>Resistance to pest</li>
</div>

