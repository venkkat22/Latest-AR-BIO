			<h3>Adaptive features of plants based on different habitats:</h3><br>
			<h4>
				<li>Hydrophytes</li></h4>
		</div>
			
			<table class="table table-striped" margin=" 0 auto;"">
				<thead>
				  <tr>
					
					<th scope="col">Adaptive features	 </th>
					<th scope="col">Explanation
					</th>
					
				</thead>
				<tbody>
				  
					
					<tr>
						
						<td>Leaf<br>
							<li>Cuticle is absent in leaves of submerged plants</li>
							<li>Stomata are absent in leaves of submerged plants</li>
						<li>Stomata are only present on upper epidermis of leaves in floating plants</li>
						<li>The upper leaf surface in floating leaves are coated with waxy cuticle</li>
						</td>
						<td><br><li>Submerged plants have no stomata</li>
							<li>Gaseous exchange for submerged plants is through diffusion of gases in water</li>
							</td>
					</tr>
					<tr>
						<td>Stem<br>
							<li>Presence of aerenchyma tissue to have air spaces</li>
							<li>Vascular tissues are poor developed</li>
						</td>
						<td><br><li>To float plants</li>
							<li>To absorb water and minerals by osmosis</li>
							</td>
						</tr>
						<tr>
							<td>Root<br>
								<li>Roots are poorly developed</li>
								</td>
							<td><br><li>Roots that anchor the soil is not necessary</li>
								</td>
						</tr>
						<tr>
							<td>Reproduction
								<br>
								<li>Reproduction is mainly by asexual methods</li>
								<li>The flowers and seeds are less abundant</li>
							</td>
							<td><br><li>Pollination seldom or unavailable under water</li>
								
								</td>
						</tr>
				</tbody>
			  </table>
