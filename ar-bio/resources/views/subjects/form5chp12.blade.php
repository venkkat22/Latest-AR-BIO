	  <div class="mybt">
		<h3>Genetics factors :</h3><br>
		1. Cross -linking during meiosis, independent selection during meiosis and random fertilization.<br><br>
		2. Gene mutations (permanent changes in the nucleotide sequence) caused by physical/chemical mutating agents.<br><br>
		3. Chromosome mutations: an increase or decrease in the number of chromosomes or changes to the structure of chromosomes.<br><br>
		4. Types of chromosomal mutations: truncation, translocation, duplication and inversion.<br><br>
		5. May cause genetic disorders; Turner syndrome, Down syndrome, trisomy X and Klinefelter syndrome.
	</div>

	<div class="mybt">
		<h3>Environmental factors :</h3><br>
		1. Includes abiotic factors; temperature, light intensity, humidity, nutrition and soil fertility.<br><br>
		2. However, environmental factors cannot change the phenotype more than what has been predetermined by the genotype.<br><br>
		3. Examples:<br>
	<li>The flowering plant (Hydrangea sp.); produces blue flowers when the soil is acidic but pink flowers in alkaline soil.</li>
	<li>Well-nourished one identical twin becomes heavier and bigger, while poorly nourished twin becomes smaller and lighter.</li>	
	</div>

	<div class="mybt">
		<h3>Interactions between genetic and environmental factors :</h3><br>
		1. Before industrialisation and the environment is not polluted, the grey-coloured moth population is higher than the other one because they can be camouflaged by the lichen-covered tree trunk.<br><br>
		2. Meanwhile, dark melanic moth dies easily, which eaten by predators because they cannot camouflage.<br><br>
		3. After industrialisation, the grey-coloured moth dies because they cannot camouflage as the pollution kills the lichen-covered three trunks.<br><br>
		4. Meanwhile, dark melanic population increases as they can be camouflaged by the polluted environment.<br><br>
		5. In such a case, the phenotype of the organism affects its chances of survival.
