			<h4>Eutrophication</h4>
			<li>Increased alga growth, mortality of aquatic life and deterioration of water quality are the effects of the eutrophication process.</li>
			<li>Eutrophication is the process of a water ecosystem becoming rich in nutrients that cause changes in the structure of the ecosystem.</li>
			<li>Excessive use of nitrate and phosphate fertilizers in agricultural activities and other factors also cause eutrophication.</li>
	        
			<br>	1. Excess nitrate and phosphate residues are discharged by rainwater into lakes and rivers. The increase in nitrate and phosphate stimulates the growth of algae so rapidly that it covers the surface of the water.<br>
			<br>	2. This condition is called eutrophication. This condition reduces the penetration of sunlight into the water.<br>
			<br>	3. The rate of photosynthesis by aquatic plants becomes low and the dissolved oxygen content in water becomes low. This causes aquatic plants and animals to die. The more algae and aquatic organisms die, the higher the population of decomposing bacteria.<br>
			<br>	4. Decomposing bacteria will decompose dead aquatic plants and animals in the presence of oxygen, causing extreme oxygen depletion in the water. This increases the level of biochemical oxygen demand or biochemical oxygen demand (BOD).<br>
            <br>
			<li>Biochemical oxygen demand (BOD) is the amount of oxygen required by microorganisms such as bacteria and fungi to break down organic matter in water.</li>
			<li>High BOD levels indicate that the quality of the water source is low and indicates that there are many organic matters as well as decomposing microorganisms in the water.</li>
			<li>Low BOD levels indicate good water quality. This is because the dissolved oxygen content is high and the water is not polluted.</li>
	      
 		</div>
