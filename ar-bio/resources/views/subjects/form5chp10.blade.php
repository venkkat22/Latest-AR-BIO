		<h3>Climate change and global warming :</h3><br>
	  <li>Climate change refers to drastic changes in the earth’s temperature, rainfall distribution and wind changes</li>
	<li>This change can be seen from the phenomenon of the greenhouse effect</li>
	<li>When sunlight enters the earth's atmosphere, part of that radiation is reflected back to the earth's atmosphere in the form of infrared rays.</li>
	<li>During the release of thermal energy, most of it will be absorbed by greenhouse gases such as carbon dioxide gas, methane gas, nitrogen oxide gas and chlorofluorocarbon (CFC) gas found in the earth’s atmosphere and prevent it from being reflected back into space.</li>
	<li>This unreflected heat energy ensures that the earth’s temperature is not too cold for the survival of the organism</li>
	<li>Most gases such as carbon dioxide gas are formed naturally.</li>
	<li>However, human activity has caused the concentration of these gases to increase and more thermal energy to be trapped, further increasing the earth’s temperature</li>
	<li>This condition is known as the greenhouse effect.</li>
	</div>
