			<div class="mybt">
				<h3>Thermal pollution :</h3>
				
				<li>Thermal pollution refers to the deterioration of water quality due to the removal of excess heat into the water due to processes that change the temperature of the water.</li>
				<li>	Water is used as a cooling agent to cool generators in industrial plants and electric power stations.</li>
				<li>	The resulting hot water is then channelled back into the river or into the lake and this causes thermal pollution.</li>
				<li>	The increase in river water temperature is not suitable for aquatic habitats.</li>
				<li>	As the water temperature rises, the dissolved oxygen content in the water decreases and complicates the survival of aquatic life.</li>
				<li>	Thermal pollution will disrupt and endanger the life of organisms and the environment.</li>
				<li>	An increase in water temperature disrupts biochemical processes in aquatic organisms because usually fish and other aquatic animals can only survive in a certain temperature range.</li>
				<li>	For example, an increase in water temperature can cause fish eggs to hatch earlier or fail to hatch.</li>
				<li>	For some species, a sudden increase in water temperature can cause immediate death</li>
				<li>	For organisms such as algae, higher temperatures will encourage their growth.</li>
				<li>	Alga growth in turn increases the BOD level of the water.</li>
		</div>

		<div class="mybt">
			<h3>Noise pollution :</h3>
			
			<li>Noise pollution is the noise that disturbs and harms human and animal health.</li>
			<li>Vehicle noise, construction activities, agricultural and industrial machinery as well as loud music from celebrations are a form of noise pollution.</li>
			<li>Continuous exposure to noise can cause stress-related health problems such as high blood pressure, cardiovascular disease, depression and headaches.</li>
	</div>

	<div class="mybt">
		<h3>Human populations growth explosion :</h3>
		
		<li>A population growth boom is an increase in the number of people living in a particular area
			<li>As population growth increases rapidly, the depletion of environmental resources also occurs rapidly, leading to deforestation, loss of biodiversity and global warming.</li>
			<li>Forest areas are being cut down to provide settlement, development, agriculture and facilities to the growing population.</li>
			<li>As a result of these activities, organisms are exposed to various threats such as problems with raw water resources and clean water supply, increased pollution and disease, and subsequent extinction of biodiversity.</li>
			<li>Demand for water and resources from the locals cannot be fully managed with the available environmental resources in the area</li>
			<li>This causes the relocation of resources from remote locations to areas of demand.</li>
			<li>In addition, sewage waste will also pose a threat of pollution to the environment.</li>
			<li>To overcome this problem, water treatment technology is used but will result in an increase in the cost of water resources including treated water.</li>
